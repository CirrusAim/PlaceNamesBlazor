    public SharedResourceLocalizer(ICurrentLocaleService localeService)
    {
        _localeService = localeService;
    }

    private IReadOnlyDictionary<string, string> GetStrings() =>
        string.Equals(_localeService.GetCurrentLocale(), "no", StringComparison.OrdinalIgnoreCase) ? No : En;

    public LocalizedString this[string name]
    {
        get
        {
            var strings = GetStrings();
            var value = strings.TryGetValue(name, out var v) ? v : name;
            return new LocalizedString(name, value, resourceNotFound: value == name);
        }
    }

    public LocalizedString this[string name, params object[] arguments]
    {
        get
        {
            var localized = this[name];
            if (localized.ResourceNotFound)
                return localized;
            var formatted = arguments?.Length > 0 ? string.Format(CultureInfo.CurrentCulture, localized.Value, arguments) : localized.Value;
            return new LocalizedString(name, formatted, false);
        }
    }

    public IEnumerable<LocalizedString> GetAllStrings(bool includeParentCultures) => GetStrings()
        .Select(kv => new LocalizedString(kv.Key, kv.Value, false));
